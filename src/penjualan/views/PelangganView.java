    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        try{
            Pelanggan plg = new Pelanggan();
            plg.setId_pel(txtId_pelanggan.getText());
            plg.setNama(txtNama_pelanggan.getText());
            
            if(RbLaki.isSelected()){
                plg.setJenis_kel(RbLaki.getText());
            }else{
                plg.setJenis_kel(RbPerempuan.getText());
            }
            
            plg.setAlamat(txtAlamat.getText());
            plg.setNo_telp(txtNo_hp.getText());
            pelanggan.insert(plg);
            this.statusAwal();
            JOptionPane.showMessageDialog(null, "Data berhasil ditambahkan");
        }catch(SQLException ex){
            Logger.getLogger(PelangganView.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_btnTambahActionPerformed

