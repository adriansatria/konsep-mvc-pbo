    void loadData(){
        try{
            record = pelanggan.getAll();
        }catch(SQLException ex){
            Logger.getLogger(PelangganView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    void isiTabel(){
        Object data[][] = new Object[record.size()][5];
        int x = 0;
        for(Pelanggan plg:record){
            data[x][0] = plg.getId_pel();
            data[x][1] = plg.getNama();
            data[x][2] = plg.getJenis_kel();
            data[x][3] = plg.getAlamat();
            data[x][4] = plg.getNo_telp();
            x++;
        }
        String[] kolom = {"Id pelanggan", "Nama pelanggan", "Jenis kelamin", "Alamat", "No. HP"};
        tabel_pelanggan.setModel(new DefaultTableModel(data, kolom));
        jScrollPane2.setViewportView(tabel_pelanggan);
    }
    
    void isiText(){
        Pelanggan plg = record.get(row);
        txtId_pelanggan.setText(plg.getId_pel());
        txtNama_pelanggan.setText(plg.getNama());
        
        if(plg.getJenis_kel().equals("L")){
            RbLaki.setSelected(true);
        }else{
            RbPerempuan.setSelected(true);
        }
        
        txtAlamat.setText(plg.getAlamat());
        txtNo_hp.setText(plg.getNo_telp());
    }
    
    void kosong(){
        txtId_pelanggan.setText("");
        txtNama_pelanggan.setText("");
        txtAlamat.setText("");
        txtNo_hp.setText("");
    }
    
    void statusAwal(){
        kosong();
        loadData();
        isiTabel();
    }

