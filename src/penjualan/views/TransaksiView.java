    private void hitungTotalBayar(){
        int jumlahRow = tabel_transaksi.getRowCount();
        int tot = 0;
        if(jumlahRow == 0){
            return;
        }else{
            for(int i = 0; i < jumlahRow; i++){
                int jumlahTotal = Integer.valueOf((String.valueOf(model.getValueAt(i, 4))));
                tot += jumlahTotal;
            }
        }
        txtTotal.setText(String.valueOf(tot));
    }

