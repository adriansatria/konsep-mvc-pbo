    private void cmbNama_barangItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbNama_barangItemStateChanged
        // TODO add your handling code here:
        String kode = (String) cmbNama_barang.getSelectedItem();
        String kodeFix = kode.substring(0, 4);
        try{
            Statement ktB = DatabaseConnection.getConnection().createStatement();
            ResultSet rsKBr = ktB.executeQuery("SELECT stok_barang, harga_barang FROM barang WHERE kode_barang = '" + kodeFix + "'");
            while(rsKBr.next()){
                txtStok.setText(rsKBr.getString("stok_barang"));
                txtHarga_barang.setText(rsKBr.getString("harga_barang"));
            }
        }catch(Exception ex){
            ex.printStackTrace();
        }
    }//GEN-LAST:event_cmbNama_barangItemStateChanged

    private void cmbNama_penggunaItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbNama_penggunaItemStateChanged
        // TODO add your handling code here:
        String kode = (String) cmbNama_pengguna.getSelectedItem();
        String kodeFix = kode.substring(0, 4);
        try{
            Statement ktB = DatabaseConnection.getConnection().createStatement();
            ResultSet rsKBr = ktB.executeQuery("SELECT alamat, notelp FROM pelanggan WHERE id_pelanggan = '" + kodeFix + "'");
            while(rsKBr.next()){
                txtAlamat_pelanggan.setText(rsKBr.getString("alamat"));
                txtNo_hp.setText(rsKBr.getString("notelp"));
            }
        }catch(Exception ex){
            ex.printStackTrace();
        }
    }//GEN-LAST:event_cmbNama_penggunaItemStateChanged

    private void btnSimpan_keranjangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpan_keranjangActionPerformed
        // TODO add your handling code here:
        String kode = (String) cmbNama_barang.getSelectedItem();
        String kodeFix = kode.substring(0, 4);
        String namaBrg = kode.substring(5, kode.length() - 0);
        int harga = Integer.valueOf(txtHarga_barang.getText());
        int jumlah = Integer.valueOf(txtStok.getText());
        long subTot = harga * jumlah;
        
        Object[] data = new Object[5];
        data[0] = kodeFix;
        data[1] = namaBrg;
        data[2] = txtHarga_barang.getText();
        data[3] = txtStok.getText();
        data[4] = subTot;
        
        model.addRow(data);
        this.hitungTotalBayar();
        this.kosongkanData();
    }//GEN-LAST:event_btnSimpan_keranjangActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        int deleteData = tabel_transaksi.getSelectedRow();
        if(deleteData == -1){
            return;
        }else{
            model.removeRow(deleteData);
            this.hitungTotalBayar();
        }
    }//GEN-LAST:event_btnHapusActionPerformed

